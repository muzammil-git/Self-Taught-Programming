#Arithmetic Operators 
print(2/2)
print(2//2) #IntegerDivision
print(2*2)
print(2-2)
print(2+2)
print(2**3) #Exponent
print(6%3)  #Modulo -> 6/3 then the remainder is 0

#Comparison Operators
print(100>10)
print(10<50)
print(10 >= 50)
print(10 <= 50)
print(10 == 10)
print(10 != 5)

#Logical Operators
print(1==1 and 2==2)
